def ceaser_cipher(message,shift_number,choice):
    alpha = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm',
            'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
    output=""
    for i in message:
        if i in alpha:
            if choice==1:
                z=alpha.index(i)+shift_number
                while z>25:
                    z=z-26
                output+=alpha[z]
            else:
                z=alpha.index(i)-shift_number
                output+=alpha[z]
        else:
            output+=i
    print(output)
    
